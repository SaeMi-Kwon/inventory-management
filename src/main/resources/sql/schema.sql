    ITEM_ID BIGINT NOT NULL,
    WAREHOUSE_ID BIGINT NOT NULL,
    HISTORY_TYPE VARCHAR(20) NOT NULL,
    INBOUND_ID BIGINT NULL,
    OUTBOUND_ID BIGINT NULL,
    BEFORE_QUANTITY INT NOT NULL DEFAULT 0,
    CHANGE_QUANTITY INT NOT NULL DEFAULT 0,
    AFTER_QUANTITY INT NOT NULL DEFAULT 0,
    PROCESS_USER_ID BIGINT NOT NULL,
    REMARK VARCHAR(255),
    CREATED_AT DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT FK_HISTORY_ITEM FOREIGN KEY (ITEM_ID) REFERENCES ITEMS (ITEM_ID),
    CONSTRAINT FK_HISTORY_WAREHOUSE FOREIGN KEY (WAREHOUSE_ID) REFERENCES WAREHOUSES (WAREHOUSE_ID),
    CONSTRAINT FK_HISTORY_USER FOREIGN KEY (PROCESS_USER_ID) REFERENCES USERS (USER_ID),
    CONSTRAINT FK_HISTORY_INBOUND FOREIGN KEY (INBOUND_ID) REFERENCES INBOUNDS (INBOUND_ID)
);